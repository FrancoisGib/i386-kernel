%macro FAULT_HANDLER_ERR_CODE 1
global fault_handler_%1
fault_handler_%1:
    cli
    push %1
    mov ecx, global_fault_handler
    jmp isr_handler
%endmacro

%macro HANDLER 2
global %1_handler_%2
%1_handler_%2:
    cli
    push 0
    push %2
    mov ecx, global_%1_handler
    jmp isr_handler
%endmacro

%macro FAULT_HANDLER_NO_ERR_CODE 1
HANDLER fault, %1
%endmacro

